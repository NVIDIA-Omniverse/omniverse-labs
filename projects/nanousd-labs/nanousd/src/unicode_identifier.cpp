Utf8DecodeResult DecodeUtf8(std::string_view text, size_t pos) {
    if (pos >= text.size()) return {};

    const auto byte0 = static_cast<unsigned char>(text[pos]);
    if (byte0 < 0x80u) return {byte0, 1, true};

    size_t length = 0;
    uint32_t codePoint = 0;
    uint32_t minCodePoint = 0;

    if (byte0 >= 0xC2u && byte0 <= 0xDFu) {
        length = 2;
        codePoint = byte0 & 0x1Fu;
        minCodePoint = 0x80u;
    } else if (byte0 >= 0xE0u && byte0 <= 0xEFu) {
        length = 3;
        codePoint = byte0 & 0x0Fu;
        minCodePoint = 0x800u;
    } else if (byte0 >= 0xF0u && byte0 <= 0xF4u) {
        length = 4;
        codePoint = byte0 & 0x07u;
        minCodePoint = 0x10000u;
    } else {
        return {};
    }

    if (pos + length > text.size()) return {};
    for (size_t i = 1; i < length; ++i) {
        const auto byte = static_cast<unsigned char>(text[pos + i]);
        if (!IsContinuationByte(byte)) return {};
        codePoint = (codePoint << 6) | (byte & 0x3Fu);
    }

    if (codePoint < minCodePoint) return {};
    if (codePoint > 0x10FFFFu) return {};
    if (codePoint >= 0xD800u && codePoint <= 0xDFFFu) return {};
    return {codePoint, length, true};
}

bool IsXidStart(uint32_t codePoint) {
    return IsInRanges(std::begin(kXidStartRanges),
                      std::end(kXidStartRanges),
                      codePoint);
}

bool IsXidContinue(uint32_t codePoint) {
    return IsInRanges(std::begin(kXidContinueRanges),
                      std::end(kXidContinueRanges),
                      codePoint);
}

bool IsIdentifierStart(uint32_t codePoint) {
    return codePoint == 0x5Fu || IsXidStart(codePoint);
}

bool IsIdentifierContinue(uint32_t codePoint) {
    return IsXidContinue(codePoint);
}

bool IsIdentifierStartAt(std::string_view text, size_t pos, size_t* length) {
    auto decoded = DecodeUtf8(text, pos);
    if (!decoded.valid || !IsIdentifierStart(decoded.codePoint)) return false;
    if (length) *length = decoded.length;
    return true;
}

bool IsIdentifierContinueAt(std::string_view text, size_t pos, size_t* length) {
    auto decoded = DecodeUtf8(text, pos);
    if (!decoded.valid || !IsIdentifierContinue(decoded.codePoint)) return false;
    if (length) *length = decoded.length;
    return true;
}

