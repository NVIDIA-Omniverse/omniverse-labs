def _generate_reference_keyframes():
    """Generate reference keyframes for the bouncing ball."""
    results = simulate_bouncing_ball(stop_time=3.0, comm_dt=0.01)
    # Sample every 10th step (i.e. every 0.1 s)
    keyframes = []
    for i in range(0, len(results), 10):
        t, h, v = results[i]
        keyframes.append((round(t, 4), round(h, 10), round(v, 10)))
    return keyframes


