"""Headless regression test for the bouncing ball FMU simulation logic.

This test verifies the bouncing ball trajectory using a pure-Python
reimplementation of the FMU logic (see src/bouncing_ball_fmu.cpp).
No FMU binary, ovrtx, or ovphysx is required.
"""

import math

import pytest


def simulate_bouncing_ball(
    h0: float = 1.0,
    v0: float = 0.0,
    g: float = -9.81,
    e: float = 0.7,
    v_min: float = 0.1,
    stop_time: float = 3.0,
    inner_dt: float = 1e-3,
    comm_dt: float = 1e-2,
) -> list[tuple[float, float, float]]:
    """Simulate the bouncing ball and return (time, h, v) at each comm step.

    Mirrors the FMU DoStep logic:
    - Outer loop advances by comm_dt (communication step size).
    - Inner loop uses 1 ms sub-steps (min of inner_dt and remaining time).
    - Bounce: when h <= 0, clamp h=0, reverse v with restitution, stop if |v| < v_min.
    """
    h = h0
    v = v0
    sim_time = 0.0
    results = [(sim_time, h, v)]

    while sim_time < stop_time - 1e-12:
        target_time = sim_time + comm_dt

        while sim_time < target_time - 1e-12:
            dt = min(inner_dt, target_time - sim_time)
            v += g * dt
            h += v * dt

            if h <= 0.0:
                h = 0.0
                if v < 0.0:
                    v = -e * v
                    if abs(v) < v_min:
                        v = 0.0

            sim_time += dt

        results.append((sim_time, h, v))

    return results


